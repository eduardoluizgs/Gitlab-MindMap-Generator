<node COLOR="#990000" CREATED="1196983377809" ID="Freemind_Link_1338320981" MODIFIED="1196983588503" TEXT="Do I have an introduction which provides an overview of the structure of my argument?">
<richcontent TYPE="NOTE"><html>
  <head>
    
  </head>
  <body>
    <p class="western" style="margin-left: 1.27cm">
      <font size="4" color="#000000">It is not enough e.g. to <i>say </i>that &#8220;I will be looking at arguments on both sides of this issue and coming to a conclusion&#8221;. You should tell us <i>which </i>arguments you will be looking at, <i>what</i>your evaluation of each of these arguments will be, and <i>how</i>this analysis justifies the overall main claim you will be making. There are two reasons to give an overview of the structure of your argument: (a) it makes it much easier for the reader to grasp what you are saying, and why; (b) writing a summary of the structure of your argument is a good way of testing that <i>you do in fact </i>have a coherent argument. </font>
    </p>
  </body>
</html>
</richcontent>
